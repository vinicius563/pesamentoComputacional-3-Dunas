# Condicionais em Python
# Condicionais são estruturas que permitem executar diferentes blocos de código com base em condições.
# Estruturas condicionais
# if = se
# else = senão
# elif = se não, se
# Exemplo de uso de condicionais
# Verificando a idade de uma pessoa
idade = 22
if idade == 18:
    print("Você é maior de idade")     
elif idade < 18:
    print("Você é menor de idade")
else:
    print("Você é maior de idade, mas não tem 18 anos")