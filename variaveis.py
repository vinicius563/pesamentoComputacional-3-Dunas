# Variáveis em Python
# Variável é um espaço na memória do computador que armazena um valor
# = é o operador de atribuição, que atribui o valor à variável
nome = "vinicius"
curso = "Eng. Elétrica"
anoCurso = "2° Semestre"
ira = 8.5
print(nome,curso,anoCurso,ira)
print(f"meu nome é {nome}, estou no curso de {curso}, no {anoCurso} e meu IRA é {ira}.")
idade = 20  # idade é uma variável que armazena o valor 20
print (idade)  # imprime o valor da variável idade
altura = 1.75  # altura é uma variável que armazena o valor 1.75
print (altura)  # imprime o valor da variável altura
print (f"meu nome é {nome}, tenho {idade} anos e minha altura é {altura}m.")